# Python allows you to assign values to multiple variables in one line
x, z = "Orange", "Banana"
print(x)
print(z)

# or you can assign the same value to multiple variables on the same line
a = b = c = "Potato"
print(a)
print(b)

# There is also a thing called unpacking where you assign items in a list to variables
people = ["John", "Jacob", "James"]
d, e, f = people
print(e)

